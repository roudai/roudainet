    <p>
      エッジ・コーナーそれぞれを、まず向きを合わせ(Edge(Corner) Orientation)、その後場所を合わせる(Edge(Corner) Permutation)というものです。ルービックキューブを目隠しで揃えるということが世界に広まったときに、最初に提案されたのがこの方法です。
    </p>
    <p>
      この解法は、考え方自体が分かりやすい、覚える手順が少なくて済むといった利点はありますが、記憶時に向き、場所を別々に覚えるため、
      <ul>
        <li>最低でも同じパーツを2巡して記憶する必要がある。</li>
        <li>ステップが多いため、手数が多くなる。</li>
      </ul>
      といったデメリットがあり、今の時代に目隠しを始めるにおいて、この解法を選ぶ理由は全くありません。
    </p>
